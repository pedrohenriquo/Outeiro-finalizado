<script type="text/javascript">

    const $body = document.querySelector( 'body' )

    document.querySelectorAll( '#imagemc' ).forEach( el => {
        
        el.addEventListener( 'click', ev => {

            el.nextElementSibling.classList.add( 'open' )

            $body.classList.add( 'open-modal-galeria' )

            if ( document.querySelector( '.carousel.open > div' ).classList.contains( 'slick-initialized' ) ) {

                $( '.carousel.open > div' ).slick( 'unslick' )
            }
            
            $( '.carousel.open > div' ).slick({
                slidesToScroll: 1,
                slidesToShow: 1,
                dots: false,
                infinite: true,
                speed: 500,
                fade: true,
                cssEase: 'linear'
            })

            document.querySelector( '.modal-galeria-overflow' ).addEventListener( 'click', ev => {

                $body.classList.remove( 'open-modal-galeria' )

                retiraOpenCarousel()
            })

            document.querySelector( '.modal-galeria-close' ).addEventListener( 'click', ev => {
                
                $body.classList.remove( 'open-modal-galeria' )
                
                retiraOpenCarousel()
            })

            
        // $('#carouselc > div').slick({
        //     slidesToScroll: 1,
