        //     dots: false,
        //     infinite: true,
        //     speed: 500,
        //     fade: true,
        //     cssEase: 'linear'
        // })
        })
    })

    function retiraOpenCarousel() {

        document.querySelectorAll( '.carousel' ).forEach( el => {

            if ( el.classList.contains( 'open' ) ) {

                el.classList.remove( 'open' )
            }
        })
    }

